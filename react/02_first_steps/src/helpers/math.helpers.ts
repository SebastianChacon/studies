export const add = (a: number, b: number): number => {
  return a + b;
};

export const subtract = (a: number, b: number): number => {
  return a - b;
};

export const multiple = (a: number, b: number): number => {
  return a * b;
};

export const divider = (a: number, b: number): number => {
  return a / b;
};
